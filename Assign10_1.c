/*
    Algorithm

    START
        Accept radius of circle from user as Radius
        Calculate Area = PI * Radius * Radius
        Display Area
    STOP
*/

///////////////////////////////////////////////////////////////////////////////////////////////
//
//     Required Header Files
//
///////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : CircleArea
//  Description   : Used to calculate area of circle
//  Input         : Float
//  Output        : Double (Returns area)
//  Author        : Shradha Dattatray Taskar
//  Date          : 03/11/2025
//
///////////////////////////////////////////////////////////////////////////////////////////////

double CircleArea(float fRadius)
{
    double dArea = 0.0;
    const float PI = 3.14;

    dArea = PI * fRadius * fRadius;

    return dArea;
}

///////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function
//
///////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    float fValue = 0.0;
    double dRet = 0.0;

    printf("Enter radius: ");
    scanf("%f", &fValue);

    dRet = CircleArea(fValue);

    printf("Area of circle is: %.4lf\n", dRet);

    return 0;
}

