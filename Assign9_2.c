/*
    Algorithm

    START
        Accept amount in USD from user as Dollar
        Multiply Dollar by 70
        Return INR value
    STOP
*/

///////////////////////////////////////////////////////////////////////////////////////////////
//
//     Required Header Files
//
///////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////
//
//  Function Name : DollarToINR
//  Description   : Used to convert USD to INR (1 USD = 70 INR)
//  Input         : Integer
//  Output        : Integer (Returns value in INR)
//  Author        : Shradha Dattatray Taskar
//  Date          : 03/11/2025
//
///////////////////////////////////////////////////////////////////////////////////////////////

int DollarToINR(int iNo)
{
    int iINR = 0;
    iINR = iNo * 70;
    return iINR;
}

///////////////////////////////////////////////////////////////////////////////////////////////
//
//  Entry point function
//
///////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0, iRet = 0;

    printf("Enter number of USD: ");
    scanf("%d", &iValue);

    iRet = DollarToINR(iValue);

    printf("Value in INR is %d\n", iRet);

    return 0;
}

